package day04_01;
public class Day04_03 {
	public static void main(String[] args) {
		// 정수형 변수를 선언하고 출력함
		int var1 = 10;
		System.out.println(var1);
		// 8진 정수형 변수를 선언하고 출력함
		int var2 = 010;
		System.out.println(var2);
		// 16진 정수형 변수를 선언하고 출력함
		int var3 = 0x10;
		System.out.println(var3);
	}
}